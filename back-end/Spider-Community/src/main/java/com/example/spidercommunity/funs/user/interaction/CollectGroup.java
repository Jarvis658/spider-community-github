package com.example.spidercommunity.funs.user.interaction;

public class CollectGroup {
    private String collect_group_id;
    private String collect_group_name;
    private Integer post_number;
    private Integer collect_status;

    public String getCollect_group_id() {
        return collect_group_id;
    }

    public void setCollect_group_id(String collect_group_id) {
        this.collect_group_id = collect_group_id;
    }

    public String getCollect_group_name() {
        return collect_group_name;
    }

    public void setCollect_group_name(String collect_group_name) {
        this.collect_group_name = collect_group_name;
    }

    public Integer getPost_number() {
        return post_number;
    }

    public void setPost_number(Integer post_number) {
        this.post_number = post_number;
    }

    public Integer getCollect_status() {
        return collect_status;
    }

    public void setCollect_status(Integer collect_status) {
        this.collect_status = collect_status;
    }
}
